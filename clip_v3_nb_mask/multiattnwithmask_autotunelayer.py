def slerp(low, high, val):
    ori_shape = low.shape
    low = low.reshape((ori_shape[0]*ori_shape[1], ori_shape[2]))
    high = high.reshape((ori_shape[0]*ori_shape[1], ori_shape[2]))

    # low_norm = low/torch.norm(low, dim=1, keepdim=True)
    # high_norm = high/torch.norm(high, dim=1, keepdim=True)
    omega = torch.acos((low*high).sum(1))
    so = torch.sin(omega)
    res = (torch.sin((1.0-val)*omega)/so).unsqueeze(1)*low + (torch.sin(val*omega)/so).unsqueeze(1) * high
    res = res.reshape(ori_shape)
    return res

def cosine_sim(x,y,pairwise=False):  
    
    x /= x.norm(dim=-1, keepdim=True)
    y /= y.norm(dim=-1, keepdim=True)
    
    if pairwise: 
        similarity = torch.sum(x * y, dim=1)
    else: similarity = x @ y.T
    return similarity

